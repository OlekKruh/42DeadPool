int	ft_str_is_alpha(char *str)
{
	int	j;
	int	flag;

	flag = 0;
	j = 0;
	while (str[j] != '\0')
	{
		if (('a' <= str[j] && str[j] <= 'z')
			|| ('A' <= str[j] && str[j] <= 'Z'))
		{
			flag++;
		}
		j++;
	}
	if (flag == j)
	{
		return (1);
	}
	else
	{
		return (0);
	}
}
