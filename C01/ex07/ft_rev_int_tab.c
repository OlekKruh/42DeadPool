void	ft_rev_int_tab(int *tab, int size)
{
	int	i;
	int	r;
	int	temp;

	i = 0;
	r = size - 1;
	while (i < r)
	{
		temp = tab[i];
		tab[i] = tab[r];
		tab[r] = temp;
		i++;
		r--;
	}
}
