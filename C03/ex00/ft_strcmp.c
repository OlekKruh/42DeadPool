#include <stdio.h>

int	ft_strcmp(char *s1, char *s2)
{
	int	id;

	id = 0;
	while (s1[id] || s2[id])
	{
		if (s1[id] != s2[id])
			return (s1[id] - s2[id]);
		id++;
	}
	return (0);
}
