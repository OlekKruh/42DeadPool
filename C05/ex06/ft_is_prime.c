int	ft_is_prime(int nb)
{
	int	i;

	i = 0;
	if (nb < 0 || nb == 0 || nb == 1)
		return (0);
	if (nb == 2 || nb == 3)
		return (1);
	while (i != nb)
	{
		if ((nb % 2 == 0) || (nb % 3 == 0))
		{
			return (0);
		}
		i++;
	}
	return (1);
}
