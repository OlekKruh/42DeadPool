int	ft_find_next_prime(int nb)
{
	int	i;

	i = 0;
	if (nb < 0 || nb == 0 || nb == 1)
		return (2);
	if (nb == 2)
		return (3);
	while (((nb + i) % 2 == 0) || ((nb + i) % 3 == 0))
	{
		i++;
	}
	return (nb + i);
}
